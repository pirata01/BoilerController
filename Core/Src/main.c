/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_USART1_UART_Init();
  MX_SPI1_Init();
  MX_RTC_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  //DMA for analog temperature sensors
  HAL_ADC_Start_DMA(&hadc1, buffer_ADC, 2);
  //DMA for UART
  HAL_UART_Receive_DMA(&huart1, rx_buff, 64);
  //Initialization for SSD1300
  HAL_I2C_Init(&hi2c1);
  SSD1306_Init(hi2c1);
  HAL_Delay(100);
  SSD1306_Fill(0x00);
  SSD1306_Flush(hi2c1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  //one second alarm
	  if(flag_alarmsec)
	  {
		  flag_alarmsec = 0;

		  //TO OLED
		  get_datetime();
		  SSD1306_Fill(0x00);
		  sprintf((char*)buffer_OLED, "Time: %s", (uint8_t *)time);
		  LCD_PutStr(0, 0, buffer_OLED ,fnt7x10);
		  sprintf((char*)buffer_OLED, "Date: %s", date);
		  LCD_PutStr(0, 14, buffer_OLED ,fnt7x10);
		  sprintf((char*)buffer_OLED, "H2O : %.1f", CalculateSteinhart(temperatures_NTC[0]));
		  LCD_PutStr(0, 34, buffer_OLED ,fnt7x10);
		  sprintf((char*)buffer_OLED, "AIR : %.1f", CalculateSteinhart(temperatures_NTC[1]));
		  LCD_PutStr(0, 48, buffer_OLED ,fnt7x10);
		  SSD1306_Flush(hi2c1);

		  //TO BLUETOOTH
		  sprintf((char*)buffer_TXhuart1, "%d,%05.1f,%05.1f,%s", LIVEDATA, CalculateSteinhart(temperatures_NTC[0]), CalculateSteinhart(temperatures_NTC[1]), datetime);
		  HAL_UART_Transmit(&huart1, buffer_TXhuart1, LIVADATA_SIZE, 100);
	  }

	  //ten second alarm
	  if(flag_alarmmin >= 10)
	  {
		  flag_alarmmin = 0;
		  HAL_UART_Transmit(&huart2, (uint8_t *)"Timer 10 timeout.\n", 18, 100);
	  }

	  //FROM BLUETOOTH
	  if(flag_BTRX == 1)
	  {
		  switch((int)(buffer_RXhuart1[0] - '0'))
		  {
		  	  case LIVEDATA:
		  	  {
		  		  char* token;
		  		  uint8_t i = 0;
		  		  uint32_t test[6] = {};

				  token = strtok ((char*)buffer_RXhuart1,",");
				  while (token != NULL)
				  {
					  if(i >= 1 && i <= 6)
						  test[i - 1] = atoi(token);

					  i++;
					  token = strtok (NULL, ",");
				  }
				  set_datetime(test);
		  		  break;
		  	  }
		  	  default:
		  		  break;
		  }
		  flag_BTRX = 0;
	  }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  HAL_Delay(100);
  }
  /* USER CODE END 3 */
}

